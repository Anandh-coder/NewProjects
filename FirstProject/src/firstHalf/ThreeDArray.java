package firstHalf;

public class ThreeDArray {

	public static void main(String[] args) {
		
		int[][][] moon = {{{1, 2, 3},{4, 5, 6},{7, 8, 90}}};
		System.out.println(moon[0][1][2]);
		
		//Enhanced for loops
		//earth is collection or data structure
		int[] earth = {6,2,5,8,9,63,25};
		for(int i : earth) {
			System.out.println(i);
		}

	}

}
